-- ============================================================
-- 02_ロールとコンテキスト切替.sql
-- Snowflake DWW Badge 1 - ロール切替とワークシートコンテキスト
-- ============================================================
-- 【概要】
-- Snowflake では「誰として操作するか」を USE ROLE で切り替える。
-- また、USE DATABASE / USE SCHEMA / USE WAREHOUSE でコンテキストを
-- 設定することで、クエリ内でオブジェクト名を省略できる。
--
-- ワークシートの4つのドロップダウン：
--   ① ROLE      ← 必須。これがないとクエリ実行不可
--   ② WAREHOUSE ← 必須。コンピューティングリソースを指定
--   ③ DATABASE  ← 任意。ホームベース（省略時は完全修飾名が必要）
--   ④ SCHEMA    ← 任意。ホームベース（省略時は完全修飾名が必要）
-- ============================================================


-- ============================================================
-- 1. ロールの切り替え
-- ============================================================
-- 【目的】現在のセッションで使うロールを変える
-- 【動作】USE ROLE を実行したロールで以降のすべての操作が行われる
-- 【注意】ログアウト・再ログインするとデフォルトロールに戻る

-- 最高権限ロール（API Integration などの操作に必要）
use role accountadmin;

-- 一般的なオブジェクト作成に使うロール
use role sysadmin;


-- ============================================================
-- 2. データベース・スキーマのコンテキスト切替
-- ============================================================
-- 【目的】以降のクエリで DB名・スキーマ名を省略できるようにする
-- 【動作】USE DATABASE を実行すると、テーブル名だけでクエリを書ける
-- 　　　  例: USE DATABASE UTIL_DB → SELECT * FROM PUBLIC.MY_TABLE

use database UTIL_DB;
use schema PUBLIC;

-- 完全修飾名の例（コンテキスト設定不要で必ず動く書き方）
-- SELECT * FROM UTIL_DB.PUBLIC.some_table;


-- ============================================================
-- 3. ウェアハウスの切り替え
-- ============================================================
-- 【目的】クエリ実行に使うコンピューティングリソースを指定する
-- 【動作】USE WAREHOUSE なしでクエリを実行するとエラーになる
-- 【注意】XSサイズ推奨（コスト効率が高い）

use warehouse COMPUTE_WH;


-- ============================================================
-- 4. 複合コンテキスト設定（DORA チェック時の典型パターン）
-- ============================================================
-- 【目的】DORA採点関数（grader）を実行するために必要な設定
-- 【注意】DORA コードを実行する前は必ずこの3行を先に実行すること

use database UTIL_DB;
use schema PUBLIC;
use role ACCOUNTADMIN;

-- ============================================================
-- 【"Does Not Exist" エラーのトラブルシューティング手順】
-- ============================================================
-- 1. ロールを確認（第1ルール）→ USE ROLE で正しいロールに切替
-- 2. コンテキストを確認      → USE DATABASE / USE SCHEMA を確認
-- 3. タイポを確認            → オブジェクト名を SHOW コマンドで照合
-- 4. そもそも作成したか確認  → INFORMATION_SCHEMA で検索
-- ============================================================
