-- ============================================================
-- 01_show_commands.sql
-- 対応まとめ: 02_UI_Notebooks.md § 2.5 SHOW コマンド
-- 目的: SHOW コマンドの各バリエーションと出力の違いを確認する
-- ============================================================

-- ============================================================
-- STEP 1: データベース一覧（現在のロールがアクセス可能なもの）
-- ============================================================
SHOW DATABASES;

-- ============================================================
-- STEP 2: スキーマ一覧の違いを確認する
-- ============================================================

-- (A) 現在のDatabaseコンテキスト内のスキーマ（コンテキスト依存）
USE DATABASE SNOWFLAKE_SAMPLE_DATA;
SHOW SCHEMAS;
-- → SNOWFLAKE_SAMPLE_DATA のスキーマだけ表示される

-- (B) アカウント全体のスキーマ（コンテキスト不要）
SHOW SCHEMAS IN ACCOUNT;
-- → すべてのDBのスキーマが表示される（件数の違いに注目）

-- ============================================================
-- STEP 3: テーブル一覧
-- ============================================================
SHOW TABLES;                                    -- 現在のスキーマ内
SHOW TABLES IN SCHEMA SNOWFLAKE_SAMPLE_DATA.TPCH_SF1;  -- スキーマ指定

-- ============================================================
-- STEP 4: Warehouse・ロール・ユーザー一覧
-- ============================================================
SHOW WAREHOUSES;
SHOW ROLES;
SHOW USERS;

-- ============================================================
-- STEP 5: 関数一覧（UDFの確認などに使用）
-- ============================================================
SHOW FUNCTIONS IN ACCOUNT;
-- → 大量に出るので、フィルタ（LIKE句）を活用する
SHOW FUNCTIONS LIKE 'ST_%' IN ACCOUNT;   -- 地理空間関数だけ表示

-- ============================================================
-- STEP 6: SHOW コマンドの結果を SELECT で操作する
-- LAST_QUERY_ID() を使って直前の SHOW 結果を取得する
-- ============================================================
SHOW DATABASES;
SELECT "name", "owner", "retention_time"
FROM TABLE(RESULT_SCAN(LAST_QUERY_ID()))
WHERE "name" NOT LIKE 'SNOWFLAKE%'
ORDER BY "name";
-- → RESULT_SCAN を使うと SHOW の結果をフィルタ・集計できる
