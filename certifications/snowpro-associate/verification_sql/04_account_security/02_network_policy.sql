-- ============================================================
-- 02_network_policy.sql
-- 対応まとめ: 04_アカウント_セキュリティ.md § 3. ネットワークセキュリティ
-- 目的: Network Policy の作成・確認・適用・削除を体験する
-- 注意: アカウント全体への適用は影響が大きいため STEP 5 は慎重に実行する
--       本番環境では自分の現在の IP が ALLOWED_IP_LIST に含まれることを確認してから適用すること
-- ============================================================

-- ============================================================
-- STEP 1: 現在の自分の IP アドレスを確認（ポリシー作成前に確認必須）
-- ============================================================
SELECT SYSTEM$ALLOWLIST();
-- → アカウントへの接続元 IP が確認できる

-- ============================================================
-- STEP 2: ネットワークポリシーの作成
-- ALLOWED_IP_LIST: 許可する IP 範囲（CIDR 記法可）
-- BLOCKED_IP_LIST: 明示的に拒否する IP（ALLOWED より優先）
-- ============================================================
CREATE NETWORK POLICY verify_policy
  ALLOWED_IP_LIST = ('0.0.0.0/0')   -- 全 IP を許可（テスト用）
  COMMENT = '検証用ネットワークポリシー';

-- ============================================================
-- STEP 3: ネットワークポリシーの一覧確認
-- ============================================================
SHOW NETWORK POLICIES;

-- ============================================================
-- STEP 4: ポリシーの詳細確認
-- ============================================================
DESC NETWORK POLICY verify_policy;

-- ============================================================
-- STEP 5: 特定ユーザーへの適用（影響範囲を限定）
-- アカウント全体への適用は省略（本番では慎重に実行）
-- ============================================================
-- ALTER USER 自分のユーザー名 SET NETWORK_POLICY = verify_policy;

-- 適用後の確認
-- SHOW PARAMETERS LIKE 'NETWORK_POLICY' IN USER 自分のユーザー名;

-- ============================================================
-- STEP 6: ポリシーの変更（IP 範囲を追加）
-- ============================================================
ALTER NETWORK POLICY verify_policy SET
  ALLOWED_IP_LIST = ('0.0.0.0/0', '10.0.0.0/8');

DESC NETWORK POLICY verify_policy;

-- ============================================================
-- STEP 7: ユーザーからポリシーを解除
-- ============================================================
-- ALTER USER 自分のユーザー名 UNSET NETWORK_POLICY;

-- ============================================================
-- STEP 8: 後片付け
-- ============================================================
DROP NETWORK POLICY IF EXISTS verify_policy;
